#include "../../includes/philo.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	n;

	i = 0;
	n = 0;
	while (str[i] == ' ' || str[i] == '\t' || str[i] == '\n'
		|| str[i] == '\r' || str[i] == '\v' || str[i] == '\f')
		i++;
	while (str[i])
	{
		n = n * 10 + (str[i] - 48);
		if (str[i] <= '0' || str[i] >= '9')
			return (0);
		i++;
	}
	return (n);
}
